char	*ft_strlowcase(char *str)
{
	char	temp;
	int		i;

	i = 0;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			temp = *str;
			*str = temp + ('a' - 'A');
		}
		str++;
		i++;
	}
	return (str - i);
}

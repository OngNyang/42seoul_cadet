char	*ft_strupcase(char *str)
{
	char	temp;
	int		i;

	i = 0;
	while (*str != '\0')
	{
		if (*str >= 97 && *str <= 122)
		{
			temp = *str;
			*str = temp - 32;
		}
		str++;
		i++;
	}
	return (str - i);
}
